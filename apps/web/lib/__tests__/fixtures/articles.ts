// Helper: repeat text paragraphs to reach target length
function padToLength(base: string, target: number): string {
  let result = base;
  let i = 0;
  while (result.length < target) {
    result += `\n\n续第${i + 1}段：` + base.split("\n\n")[i % base.split("\n\n").length];
    i++;
  }
  return result;
}

